import * as chars from './chars';

export const n = chars.n.length;
export const a = chars.a.length;
export const p = chars.p.length;
export const t = chars.t.length;
export const i = chars.i.length;
export const e = chars.e.length;

export const na = n * a;
export const pt = p * t;
export const ie = i * e;

export const apt = a * pt;
export const aptie = apt * ie;
export const ptie = pt * ie;
export const tie = t * ie;
